#include <iostream>
using namespace std;

int main() {
    int a = 23;
    int b = 42;

    cout << "Equal to Equal to: " << (a == b) << endl;
    cout << "Greater Than Equal to: " << (a >= b) << endl;
    cout << "Greater Than: " << (a > b) << endl;
    cout << "Less Than: " << (a < b) << endl;
    cout << "Less Than Equal to: " << (a <= b) << endl;
    cout << "Not Equal to: " << (a != b) << endl;

    return 0;
}
